export function getUpcomingMatches(limit?: number): Match[] {
  const upcoming = matches
    .filter((match) => match.status === "upcoming")
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
  return limit ? upcoming.slice(0, limit) : upcoming;
}

export function getPastMatches(limit?: number): Match[] {
  const past = matches
    .filter((match) => match.status === "past")
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
  return limit ? past.slice(0, limit) : past;
}

export function getMatchesByTeam(team: MatchTeam): Match[] {
  return matches
    .filter((match) => match.team === team)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
}
